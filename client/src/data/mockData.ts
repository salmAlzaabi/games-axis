const buildMessage = (
  id: string,
  authorId: string,
  content: string,
  minutesAgo: number,
  reactions: { emoji: string; count: number; me?: boolean }[] = []
): Message => {
  const author = authorId === 'me' ? currentUser : mockUsers.find(u => u.id === authorId) || mockUsers[0];
  return {
    id,
    content,
    author,
    timestamp: Date.now() - minutesAgo * 60 * 1000,
    reactions: reactions.map(r => ({ ...r, users: [], me: r.me || false })),
    attachments: [],
    embeds: [],
  };
};

